package babilas.lab1;

public class Student {
    public final long id;
    public final String name;
    public final String number;
    public final String sekcja;

    public Student(long id, String name, String number, String sekcja) {
        this.id = id;
        this.name = name;
        this.number = number;
        this.sekcja = sekcja;
    }
}
