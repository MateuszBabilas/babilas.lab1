package babilas.lab1;

public class Student {
    public final long id;
    public final String name;
    public final String number;
    public final String studentGroup;

    public Student(long id, String name, String number, String studentGroup) {
        this.id = id;
        this.name = name;
        this.number = number;
        this.studentGroup = studentGroup;
    }
}
